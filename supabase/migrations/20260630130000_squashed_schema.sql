                     '*Time:* %s', 
                     NEW.event_name, NEW.error_message, COALESCE(NEW.error_stack, 'N/A'), NEW.created_at)
    );
    
    -- Using pg_net extension to fire webhook
    BEGIN
      PERFORM net.http_post(
