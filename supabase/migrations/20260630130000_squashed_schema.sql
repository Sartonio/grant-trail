  ON "storage"."objects" FOR DELETE
  USING ("bucket_id" = 'grant-documents'::"text"
    AND "public"."storage_object_tenant_id"("name") = "public"."current_tenant_id"());

-- == receipts ===============================================================
DROP POLICY IF EXISTS "Users can view their own receipts"       ON "storage"."objects";
DROP POLICY IF EXISTS "Users can upload receipts"               ON "storage"."objects";
DROP POLICY IF EXISTS "Users can delete their own receipts"     ON "storage"."objects";
DROP POLICY IF EXISTS "Admins can view all receipts in storage" ON "storage"."objects";

CREATE POLICY "Tenant-scoped read of receipts"
  ON "storage"."objects" FOR SELECT
  USING ("bucket_id" = 'receipts'::"text"
    AND ("public"."storage_object_tenant_id"("name") = "public"."current_tenant_id"() OR "public"."is_super_admin"()));

CREATE POLICY "Tenant-scoped upload of receipts"
  ON "storage"."objects" FOR INSERT
  WITH CHECK ("bucket_id" = 'receipts'::"text"
    AND "public"."storage_object_tenant_id"("name") = "public"."current_tenant_id"());

CREATE POLICY "Tenant-scoped delete of receipts"
  ON "storage"."objects" FOR DELETE
  USING ("bucket_id" = 'receipts'::"text"
    AND "public"."storage_object_tenant_id"("name") = "public"."current_tenant_id"());


-- ==========================================
-- ACL corrections not expressible in a pg_dump
-- ==========================================
-- pg_dump emits GRANTs but never REVOKEs, and Supabase ships
-- `ALTER DEFAULT PRIVILEGES ... GRANT ALL ON TABLES TO anon`. So every table
-- created by this baseline silently hands `anon` the default privilege set,
-- and the dump has no way to express the REVOKEs that the pre-squash history
-- applied (20260706044339_harden_rls_and_definer_functions).
--
-- Without these lines a fresh `db reset` leaves anon holding Dxtm =
-- TRUNCATE, REFERENCES, TRIGGER, MAINTAIN on the two tables below. TRUNCATE
-- BYPASSES RLS, so an anonymous caller could empty platform_settings --
-- the singleton that drives billing product IDs and the platform-root slug.
--
-- Caught by diffing a dump of the 20-migration history against a dump of this
-- baseline; that diff is now empty. Keep these REVOKEs in any future re-squash.
REVOKE ALL ON TABLE "public"."platform_settings" FROM "anon";
REVOKE ALL ON TABLE "public"."stripe_subscription_event_cursors" FROM "anon";
